#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	c = (char)c;
	i = ft_strlen(s) + 1;
	while (i)
	{
		if (*s == c)
			return ((char *)s);
		s++;
		i--;
	}
	return (0);
}
